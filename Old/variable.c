#include<stdio.h>
int main()
{
    int i; //integer type variable declaration
    char c; // Character type variable declaration
    float f; //float type variable declaration
    double d; //double type variable declaration

    //value assign
    i = 100;
    c = 'A';
    f = 3.14;
    d = 123.3453;

    //Printing the value of i, c, f & d
    printf("%d\n",i);
    printf("%c\n",c);
    printf("%f\n",f);
    printf("%f\n",d);

    return 0;
}
